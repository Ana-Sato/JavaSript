/* Estruturas de repeticao:
while = Enquanto 
Esrutrura mais simples com teste logico no inicio */

var c = 1
while (c <= 6) {
    console.log('Tudo bem?')
    c++
}

var c = 1
while (c <= 3) {
    console.log(`Passo ${c}`)
    c++
}


 
/* Estrutura DO WHILE:
   estrutura de repeticao com teste logico no final
*/


var c = 1
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 6)





